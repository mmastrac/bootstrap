#===========================================================================
# Arguments:
#   R0: struct
#   R1: offset
#===========================================================================
:_load_record
    add r0, r1
    ld.d r0, [r0]
    ret
#===========================================================================


#===========================================================================
# Arguments:
#   R0: struct
#   R1: offset
#   R2: value
#===========================================================================
:_store_record
    add r0, r1
    st.d [r0], r2
    ret
